import UIKit

class Metodos: NSObject {

    
    
    static func getMes(mes:Int) -> String{
        var meses = ["","ENE", "FEB", "MAR", "ABR", "MAY", "JUN", "JUL", "AGO", "SEP", "OCT", "NOV", "DIC" ]
        return meses[mes]
    }
     
    
    
}

 
